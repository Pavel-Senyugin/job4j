package ru.job4j.loop;
/**
 * Class loop Factorial.
 * @author Senyugin Pavel (mister.senyugin@yandex.ru)
 * @since 18.03.2019
 */
public class Factorial {
    public int calc1(int n) {
        int result = 1 ;
        for (int i = result; i <=n; i++) {
            return result *= i;
        }
        return result;
    }
}
